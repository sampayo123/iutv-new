  <p>En informática, CRUD es el acrónimo de "Crear, Leer, Actualizar y Borrar" (del original en inglés: Create, Read, Update and Delete),
   que se usa para referirse a las funciones básicas en bases de datos o la capa de persistencia en un software.</p>

   <p>En algunos lugares, se utilizan las siglas ABM para lo mismo ("Alta, Baja y Modificación"), 
   obviando la operación de "obtener"; el acrónimo ABC para "Altas, Bajas y Cambios";
    ABML siendo la última letra (L) de "listar, listado o lectura"; ABMC siendo la 'C' de "Consulta";
     o bien CLAB que sería la traducción literal del acrónimo ("Crear, Leer, Actualizar y Borrar")
      también se llega a usar el acrónimo ABCC ("Altas, Bajas, Cambios y Consultas").</p>
   <p>También es usado el ABCDEF: "Agregar, Buscar, Cambiar, Desplegar (listar), Eliminar, Fichar (Ficha, cédula o Reporte de un registro)".</p>
